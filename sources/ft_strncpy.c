char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	int i;

	i = 0;
	while ((dest[i] = (src[n] == '\0') ? '\0' : src[i]) != '\0' && i < n - 1)
		i++;
	return (dest);
}
