char	*ft_strstr(char *str, char *to_find)
{
	int		i;
	char	r;

	i = 0;
	r = '\0';
	while (str[i] != to_find[i] && str[i] != '\0')
		r = (str[i] == to_find[i]) ? str[i] : r;
	return (r);
}
